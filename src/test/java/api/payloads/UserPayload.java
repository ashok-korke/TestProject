package api.payloads;

public class UserPayload {
	
	Object id;
	String name;
	String job;
	
	public Object getId() {
		return id;
	}
	public void setId(Object object) {
		this.id = object;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getJob() {
		return job;
	}
	public void setJob(String job) {
		this.job = job;
	}
	
	

}
